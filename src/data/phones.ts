const GRADE_RANK: Record<ConditionGrade, number> = { "A+": 0, A: 1, B: 2, C: 3 };

export function getDefaultVariant(phone: Phone): PhoneVariant {
  const inStock = phone.variants.filter((variant) => variant.isInStock);
  const candidates = inStock.length > 0 ? inStock : phone.variants;
  return [...candidates].sort((variantA, variantB) => {
    const gradeDelta = GRADE_RANK[variantA.grade] - GRADE_RANK[variantB.grade];
    if (gradeDelta !== 0) return gradeDelta;
    return variantA.priceRupees - variantB.priceRupees;
  })[0];
}

export function getMinPriceVariant(phone: Phone): PhoneVariant {
  return [...phone.variants].sort((variantA, variantB) => variantA.priceRupees - variantB.priceRupees)[0];
}

export function getMaxPrice(phone: Phone): number {
  return Math.max(...phone.variants.map((variant) => variant.priceRupees));
}

export function isPhoneInStock(phone: Phone): boolean {
  return phone.variants.some((variant) => variant.isInStock);
}

export function hasAnyOffer(phone: Phone): boolean {
  return phone.variants.some(
    (variant) => variant.originalPriceRupees > variant.priceRupees,
  );
}

export function isAnyPtaApproved(phone: Phone): boolean {
  return phone.variants.some((variant) => variant.isPtaApproved);
}

export function getFeaturedPhones(): Phone[] {
  return phones.filter((phone) => phone.isFeatured);
}

export function getPhoneBySlug(slug: string): Phone | undefined {
  return phones.find((phone) => phone.slug === slug);
}

export function getPhonesByBrand(brandSlug: string): Phone[] {
  return phones.filter((phone) => phone.brandSlug === brandSlug);
}

export function getPhonesOnOffer(): Phone[] {
  return phones.filter(hasAnyOffer);
}
